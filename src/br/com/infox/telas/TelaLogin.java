    public void logar() {
        String sql = "select * from tbusuarios where login=? and senha=?";
        try {
            /**
             * As linhas abaixo preparam a consulta ao banco em função do que
             * foi digitado nas caixas de texto. O ? é subistituido pelo
             * conteúdo das variáveis.
             */
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUsuario.getText());
            pst.setString(2, txtSenha.getText());
            // A linha abaixo executa a query
            rs = pst.executeQuery();
            // Se existir usuário e senha correspondente
            if (rs.next()) {
                // A linha abaixo obtem o conteúdo do campo perfil da tabela  tbusuario.
                String perfil = rs.getString(5);
                //System.out.println(perfil);
                // A linha abaixo faz o tratamento do perfil do usuário.
                if (perfil.equals("admin")) {
                    TelaPrincipal principal = new TelaPrincipal();
                    principal.setVisible(true);
                    TelaPrincipal.menCadUsu.setEnabled(true);
                    TelaPrincipal.menRel.setEnabled(true);
                    TelaPrincipal.lblUsuario.setText(rs.getString(2));
                    TelaPrincipal.lblUsuario.setForeground(Color.red);
                    this.dispose(); // Fecha a tela de login
                    conexao.close(); //Fecha a conexão com o banco de dados.
                } else {
                    TelaPrincipal principal = new TelaPrincipal();
                    principal.setVisible(true);
                    TelaPrincipal.lblUsuario.setText(rs.getString(2));
                    this.dispose();
                    conexao.close();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Usuário e/o senha inválido(s)");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

